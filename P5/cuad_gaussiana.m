function [error,aprox]=cuad_gaussiana(funcion,n,interv)
%Funcion que aproxima la integral en el intervalo

%Entradas: f: la funcion a la que se le desea aproximar su integral
%          n: orden en el que se calculara la aproximacion
%          intervalo: intervalo de la integral

%Salidas: aprox: la aproximacion de la integral
%         error: el error de la aproximacion

  f=sym(funcion);
  x=sym('x');
  
%Se separan los intervalos
  
  a=interv(1);
  b=interv(2);
  
  gauss=((b-a)/2)*subs(f,((b-a)*x+(b+a))/2);
  
%Calculo del error
  aprox=cerosPeso(n, gauss);
  e1=cerosPeso(n, f);
  error=abs(aprox-e1);
end 


